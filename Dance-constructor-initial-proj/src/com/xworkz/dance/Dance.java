package com.xworkz.dance;

public class Dance {
	
 private String style;
 private String song;
 
 public Dance(String style, String song) {
	this.style = style;
	this.song = song;
 }
 
 public String getStyle() {
	return style;
 }
 public void setStyle(String style) {
	this.style = style;
 }
 
 
 public String getSong() {
	return song;
 }
 public void setSong(String song) {
	this.song = song;
 }
 
 
 
}
